import assert from "node:assert/strict";
import os from "node:os";
import path from "node:path";
import { existsSync } from "node:fs";
import { mkdtemp, readFile, rm } from "node:fs/promises";
import test from "node:test";

import { createSetupConfig, type DetectedRepoContext } from "../src/cli/setup-config.ts";
import { writeSetupFiles } from "../src/cli/setup-files.ts";

test("writeSetupFiles creates local setup files without agent secrets", async (t) => {
  const repoRoot = await mkdtemp(path.join(os.tmpdir(), "agentrail-setup-"));
  const homePath = await mkdtemp(path.join(os.tmpdir(), "agentrail-home-"));
  const detectedRepo: DetectedRepoContext = {
    repoPath: repoRoot,
    remoteSlug: "oxnw/agentrail",
    defaultBranch: "main",
    gitIgnoreHasAgentrail: true,
  };

  t.after(async () => {
    await rm(repoRoot, { recursive: true, force: true });
    await rm(homePath, { recursive: true, force: true });
  });

  const config = createSetupConfig({
    cwd: repoRoot,
    detectedRepo,
    interactionMode: "interactive",
    acceptedDefaults: true,
  });

  const result = await writeSetupFiles({
    homePath,
    config,
  });

  const configPath = path.join(homePath, "config.json");
  const envExamplePath = path.join(homePath, "agent.env.example");
  const serverEnvPath = path.join(homePath, "server.env");
  const envPath = path.join(homePath, "agent.env");
  const readmePath = path.join(homePath, "README.md");
  const recipePath = path.join(homePath, "agent-recipes.md");

  assert.deepEqual(
    result.writtenPaths.sort(),
    [configPath, envExamplePath, serverEnvPath, readmePath, recipePath].sort(),
  );

  const configText = await readFile(configPath, "utf8");
  const configBody = JSON.parse(configText);
  const envExample = await readFile(envExamplePath, "utf8");
  const serverEnv = await readFile(serverEnvPath, "utf8");
  const readme = await readFile(readmePath, "utf8");

  assert.equal(configBody.mode, "server");
  assert.equal(configBody.server.baseUrl, "http://127.0.0.1:3000");
  assert.doesNotMatch(configText, /apiKey|AGENTRAIL_API_KEY/);
  assert.match(envExample, /AGENTRAIL_BASE_URL=http:\/\/127\.0\.0\.1:3000/);
  assert.match(envExample, /AGENTRAIL_API_KEY=/);
  assert.match(envExample, /AGENTRAIL_API_KEY_ID=/);
  assert.match(envExample, /AGENTRAIL_AGENT_RECIPE_PATH=~\/\.agentrail\/agent-recipes\.md/);
  assert.doesNotMatch(envExample, /GITHUB_TOKEN=/);
  assert.doesNotMatch(envExample, /CIRCLECI_TOKEN=/);
  assert.doesNotMatch(envExample, /CIRCLECI_WEBHOOK_SECRET=/);
  assert.match(serverEnv, /AGENTRAIL_AGENT_AUTH_STORE_PATH=stores\/agent-auth\.json/);
  assert.doesNotMatch(serverEnv, /AGENTRAIL_PROVIDER_IDENTITY_MAPPINGS_STORE_PATH=/);
  assert.match(serverEnv, /AGENTRAIL_TASK_STORE_PATH=stores\/tasks\.json/);
  assert.equal(existsSync(envPath), false);
  assert.match(readme, /generated by `agentrail init`/i);
  assert.match(readme, /agentrail agent create/i);
  assert.match(readme, /choose a runner, choose permissions, and create another local agent/i);
  assert.match(readme, /already signed in to the runner you choose on this machine/i);
  assert.match(readme, /server will load `~\/\.agentrail\/server\.env` automatically/i);
  assert.match(readme, /\.\/agentrail doctor/i);
  assert.match(readme, /setup is only complete when `\/tasks\/mine\?status=in_progress&limit=1` returns assigned work/i);
  assert.match(readme, /~\/\.agentrail\/agents\/<agentId>\.env/i);
  assert.match(readme, /First connected repo/i);
  assert.match(readme, /provider connect github/i);
  assert.match(readme, /provider connect circleci/i);
  assert.match(readme, /save them into `~\/\.agentrail\/provider\.env`/i);
  assert.match(readme, /does not capture live provider credentials/i);
  assert.match(readme, /AgentRail can connect more repos later with `agentrail repo add`/i);
  assert.match(readme, /--repo "\$PWD"/);
  assert.doesNotMatch(readme, /task sources/i);
});
