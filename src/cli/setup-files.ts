import path from "node:path";
import { mkdir, writeFile } from "node:fs/promises";

import { buildInitCommand, type SetupConfig } from "./setup-config.ts";

export interface WriteSetupFilesOptions {
  repoRoot: string;
  config: SetupConfig;
}

export interface WriteSetupFilesResult {
  writtenPaths: string[];
}

export async function writeSetupFiles({
  repoRoot,
  config,
}: WriteSetupFilesOptions): Promise<WriteSetupFilesResult> {
  const agentrailDir = path.join(repoRoot, ".agentrail");
  const configPath = path.join(agentrailDir, "config.json");
  const envExamplePath = path.join(agentrailDir, "agent.env.example");
  const readmePath = path.join(agentrailDir, "README.md");
  const writtenPaths = [configPath, envExamplePath, readmePath];

  await mkdir(agentrailDir, { recursive: true });
  if (config.exports.markdown.enabled) {
    await mkdir(path.join(repoRoot, config.exports.markdown.path), { recursive: true });
  }

  await writeFile(configPath, `${JSON.stringify(config, null, 2)}\n`, "utf8");
  await writeFile(envExamplePath, renderAgentEnvExample(config), "utf8");
  await writeFile(readmePath, renderSetupReadme(config), "utf8");

  return { writtenPaths };
}

function renderAgentEnvExample(config: SetupConfig): string {
  const recipePath = path.join(config.targetRepo.path, "docs", "agent-recipes.md");

  return [
    "# Generated by `agentrail init`.",
    "# `agentrail agent create` writes `.agentrail/agent.env` later with mode 0600.",
    "# Demo mode can leave the provider tokens blank.",
    `AGENTRAIL_BASE_URL=${config.server.baseUrl}`,
    "AGENTRAIL_API_KEY=",
    "AGENTRAIL_AGENT_ID=",
    "AGENTRAIL_AGENT_RUNNER=",
    `AGENTRAIL_REPO_ALLOWLIST=${config.targetRepo.allowlist.join(",")}`,
    `AGENTRAIL_AGENT_RECIPE_PATH=${recipePath}`,
    `${config.providers.github.tokenEnv}=`,
    `${config.providers.circleci.tokenEnv}=`,
    `${config.providers.circleci.webhookSecretEnv}=`,
    "",
  ].join("\n");
}

function renderSetupReadme(config: SetupConfig): string {
  return [
    "# AgentRail Local Setup",
    "",
    "This directory was generated by `agentrail init`.",
    "",
    "## Files",
    "",
    "- `config.json`: non-secret setup state for local/self-hosted runs.",
    "- `agent.env.example`: placeholder environment file until `agentrail agent create` writes `.agentrail/agent.env`.",
    "- `README.md`: setup notes and next commands.",
    "",
    "## GitHub and CircleCI",
    "",
    `- Target GitHub checkout: \`${config.targetRepo.path}\`. AgentRail works against this local checkout when it prepares task, review, and CI operations.`,
    `- GitHub repo allowlist: \`${config.targetRepo.allowlist.join(", ")}\`. Replace any local-path fallback with the real \`owner/repo\` slug before enabling live providers.`,
    "- Demo mode keeps the GitHub and CircleCI adapters local-only. Leave `GITHUB_TOKEN`, `CIRCLECI_TOKEN`, and `CIRCLECI_WEBHOOK_SECRET` blank.",
    "- Server mode expects `GITHUB_TOKEN` for GitHub submit/review/CI adapters and `CIRCLECI_TOKEN` plus `CIRCLECI_WEBHOOK_SECRET` for CircleCI status polling and webhook verification.",
    `- CircleCI should send webhooks to \`${config.server.baseUrl}/providers/circleci/webhooks\` after the server is running.`,
    "",
    "## Next Steps",
    "",
    `1. Start the local API with ${config.mode === "demo" ? "`npm run demo:server`" : "`npm start`"}.`,
    "2. Complete agent registration with `agentrail agent create` once the server-backed phase is implemented.",
    "3. If you are enabling live providers, copy the `GITHUB_TOKEN`, `CIRCLECI_TOKEN`, and `CIRCLECI_WEBHOOK_SECRET` placeholders into `.agentrail/agent.env` after registration.",
    `4. For automation or CI, you can still reproduce this setup non-interactively with: \`${buildInitCommand(config)}\`.`,
    "",
  ].join("\n");
}
